package com.gudi.project.dto;

public class OutfitDTO {
	private int board_num;
	private String cloth_num;
	private String color_num;
	
	public int getBoard_num() {
		return board_num;
	}
	public void setBoard_num(int board_num) {
		this.board_num = board_num;
	}
	public String getCloth_num() {
		return cloth_num;
	}
	public void setCloth_num(String clothData) {
		this.cloth_num = clothData;
	}
	public String getColor_num() {
		return color_num;
	}
	public void setColor_num(String color_num) {
		this.color_num = color_num;
	}
	
}
