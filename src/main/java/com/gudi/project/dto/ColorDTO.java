package com.gudi.project.dto;

public class ColorDTO {
	
	private String color_name;
	private int color_state;
	private String color_photo;
	private String color_num;
	
	public String getColor_num() {
		return color_num;
	}
	public void setColor_num(String color_num) {
		this.color_num = color_num;
	}
	public String getColor_name() {
		return color_name;
	}
	public void setColor_name(String color_name) {
		this.color_name = color_name;
	}
	public int getColor_state() {
		return color_state;
	}
	public void setColor_state(int color_state) {
		this.color_state = color_state;
	}
	public String getColor_photo() {
		return color_photo;
	}
	public void setColor_photo(String color_photo) {
		this.color_photo = color_photo;
	}
}
