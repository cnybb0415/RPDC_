package com.gudi.project.dto;

public class ClothDTO {
	private int cloth_num;
	private String cloth_type;
	private String cloth_name;
	private int cloth_state;
	private String cloth_photo;
	public int getCloth_num() {
		return cloth_num;
	}
	public void setCloth_num(int cloth_num) {
		this.cloth_num = cloth_num;
	}
	public String getCloth_type() {
		return cloth_type;
	}
	public void setCloth_type(String cloth_type) {
		this.cloth_type = cloth_type;
	}
	public String getCloth_name() {
		return cloth_name;
	}
	public void setCloth_name(String cloth_name) {
		this.cloth_name = cloth_name;
	}
	public int getCloth_state() {
		return cloth_state;
	}
	public void setCloth_state(int cloth_state) {
		this.cloth_state = cloth_state;
	}
	public String getCloth_photo() {
		return cloth_photo;
	}
	public void setCloth_photo(String cloth_photo) {
		this.cloth_photo = cloth_photo;
	}
	
}
